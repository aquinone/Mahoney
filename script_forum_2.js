const worldInitialView = {
  center: [28, 175],
  zoom: 2.5
};

const chicagoInitialView = {
  center: [41.88, -87.63],
  zoom: 9.4
};

function createMap(id, center, zoom) {

  const map = L.map(id, {
    dragging: false,
    scrollWheelZoom: false,
    doubleClickZoom: false,
    touchZoom: false,
    boxZoom: false,
    keyboard: false,
    zoomControl: false,
    worldCopyJump: true
  }).setView(center, zoom);

  L.GridLayer.RecolorTiles = L.GridLayer.extend({

    createTile(coords, done) {

      const tile = document.createElement("canvas");

      const size = this.getTileSize();

      tile.width = size.x;
      tile.height = size.y;

      const ctx = tile.getContext("2d");

      const img = new Image();

      img.crossOrigin = "anonymous";

      const s = "abcd"[Math.abs(coords.x + coords.y) % 4];

      img.src =
        `https://${s}.basemaps.cartocdn.com/light_nolabels/${coords.z}/${coords.x}/${coords.y}@2x.png`;

      img.onload = () => {

        ctx.drawImage(img, 0, 0, size.x, size.y);

        const idata =
          ctx.getImageData(0, 0, size.x, size.y);

        const d = idata.data;

        for (let i = 0; i < d.length; i += 4) {

          const dr = d[i] - 213;
          const dg = d[i + 1] - 218;
          const db = d[i + 2] - 220;

          if (dr * dr + dg * dg + db * db < 900) {

            d[i] = 23;
            d[i + 1] = 49;
            d[i + 2] = 88;
          }
        }

        ctx.putImageData(idata, 0, 0);

        done(null, tile);
      };

      img.onerror = () => done(null, tile);

      return tile;
    }
  });

  new L.GridLayer.RecolorTiles({
    attribution:
      '&copy; OpenStreetMap &copy; CARTO',
    maxZoom: 19
  }).addTo(map);

  L.tileLayer(
    "https://{s}.basemaps.cartocdn.com/light_only_labels/{z}/{x}/{y}{r}.png",
    {
      subdomains: "abcd",
      maxZoom: 19
    }
  ).addTo(map);

  return map;
}

const worldMap = createMap(
  "world-map",
  worldInitialView.center,
  worldInitialView.zoom
);

const chicagoMap = createMap(
  "chicago-map",
  chicagoInitialView.center,
  chicagoInitialView.zoom
);

const panel = document.getElementById("panel");

const overlay = document.getElementById("overlay");

const panelTitle =
  document.getElementById("panel-title");

const panelContent =
  document.getElementById("panel-content");

const panelImage =
  document.getElementById("panel-image");

const panelClose =
  document.getElementById("panel-close");

const worldMapEl =
  document.getElementById("world-map");

const chicagoMapEl =
  document.getElementById("chicago-map");

let activeMarker = null;

function openPanel(
  loc,
  marker,
  mapInstance,
  resetView
) {

  if (activeMarker) {
    activeMarker
      .getElement()
      .classList
      .remove("marker-active");
  }

  activeMarker = marker;

  marker
    .getElement()
    .classList
    .add("marker-active");

  panelTitle.textContent = loc.name;

  panelContent.innerHTML =
    loc.content
      .map(p => `<p>${p}</p>`)
      .join("");

  if (loc.image) {

    panelImage.src = loc.image;

    panelImage.alt = loc.name;

    panelImage.classList.add("visible");

  } else {

    panelImage.classList.remove("visible");
  }

  panel.classList.add("open");

  overlay.classList.add("active");

  worldMapEl.classList.add("dimmed");
  chicagoMapEl.classList.add("dimmed");

  const targetZoom =
    Math.max(mapInstance.getZoom(), 5);

  const pinPoint =
    mapInstance.project(
      [loc.lat, loc.lng],
      targetZoom
    );

  const offsetX =
    mapInstance.getSize().x * 0.22;

  const adjustedCenter =
    mapInstance.unproject(
      L.point(pinPoint.x + offsetX, pinPoint.y),
      targetZoom
    );

  mapInstance.setView(
    adjustedCenter,
    targetZoom,
    {
      animate: true,
      duration: 1.8
    }
  );

  panel.dataset.map =
    mapInstance === worldMap
      ? "world"
      : "chicago";
}

function closePanel() {

  panel.classList.remove("open");

  overlay.classList.remove("active");

  worldMapEl.classList.remove("dimmed");

  chicagoMapEl.classList.remove("dimmed");

  if (activeMarker) {

    activeMarker
      .getElement()
      .classList
      .remove("marker-active");

    activeMarker = null;
  }

  const targetMap =
    panel.dataset.map === "world"
      ? worldMap
      : chicagoMap;

  const resetView =
    panel.dataset.map === "world"
      ? worldInitialView
      : chicagoInitialView;

  targetMap.setView(
    resetView.center,
    resetView.zoom,
    {
      animate: true,
      duration: 1.8
    }
  );
}

const defaultIcon = L.icon({

  iconUrl:
    "https://raw.githubusercontent.com/pointhi/leaflet-color-markers/master/img/marker-icon-orange.png",

  iconRetinaUrl:
    "https://raw.githubusercontent.com/pointhi/leaflet-color-markers/master/img/marker-icon-2x-orange.png",

  shadowUrl:
    "https://unpkg.com/leaflet@1.9.4/dist/images/marker-shadow.png",

  iconSize: [25, 41],

  iconAnchor: [12, 41],

  tooltipAnchor: [1, -38],

  shadowSize: [41, 41]
});

const chicagoBounds = L.latLngBounds(
  [41.5, -88.1],
  [42.2, -87.3]
);

locations.forEach((loc) => {

  const isChicago =
    chicagoBounds.contains([
      loc.lat,
      loc.lng
    ]);

  const targetMap =
    isChicago
      ? chicagoMap
      : worldMap;

  const resetView =
    isChicago
      ? chicagoInitialView
      : worldInitialView;

  const marker = L.marker(
    [loc.lat, loc.lng],
    { icon: defaultIcon }
  ).addTo(targetMap);

  marker.bindTooltip(
    loc.name,
    {
      direction: "top"
    }
  );

  marker.on(
    "click",
    () => openPanel(
      loc,
      marker,
      targetMap,
      resetView
    )
  );
});

panelClose.addEventListener(
  "click",
  closePanel
);

overlay.addEventListener(
  "click",
  closePanel
);
